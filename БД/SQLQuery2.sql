SELECT Заказы.Дата_поставки
FROM   Заказчики INNER JOIN
             Заказы ON Заказчики.Наименование_фирмы = Заказы.Заказчик INNER JOIN
             Товары ON Заказы.Наименование_товара = Товары.Наименование
WHERE (Заказы.Дата_поставки > CONVERT(DATETIME, '2022-04-01 00:00:00', 102))

SELECT Наименование_товара, Дата_поставки
FROM   Заказы
WHERE (Дата_поставки > CONVERT(DATETIME, '2022-07-06 00:00:00', 102))